//Function to return a tree created from postorder and inoreder traversals.

Node * solve(int in[], int post[], int i, int n, int s){
    if(i>=n) return NULL;
    Node * root = new Node(post[s]);
    int r = post[s];
    int j = n-1;
    int l = s-1;
    while(in[j]!=r){
        j--;
        s--;
    }
    root->left = solve(in,post,i,j,s-1);
    root->right = solve(in,post,j+1,n,l);
    
}

